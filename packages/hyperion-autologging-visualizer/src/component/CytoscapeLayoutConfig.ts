export function getCytoscapeLayoutConfig(name: 'klay' | 'elk' | 'dagre' | 'cola'): cytoscape.LayoutOptions {
  let CONFIG: typeof KLAY_CONFIG |
    typeof ELK_CONFIG |
    typeof DAGRE_CONFIG |
    typeof COLA_CONFIG |
    null = null;
  if (name === 'klay') {
    console.log('[PS] using klay');
    Cytoscape.use(klay);
    CONFIG = KLAY_CONFIG;
  } else if (name === 'elk') {
    console.log('[PS] using elk');
    Cytoscape.use(elk);
    CONFIG = ELK_CONFIG;
  } else if (name === 'dagre') {
    console.log('[PS] using dagre');
    Cytoscape.use(dagre);
    CONFIG = DAGRE_CONFIG;
  } else if (name === 'cola') {
    console.log('[PS] using cola');
    Cytoscape.use(cola);
    CONFIG = COLA_CONFIG;
  }

  return ({
    name,
    ...CONFIG,
  });
}

